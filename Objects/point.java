public class point {
    private int x = 0;
    private int y = 0;

    public point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getter and Setter methods for x and y

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
